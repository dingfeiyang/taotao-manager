package com.taotao.controller;

public class User {
	private String code;
	
	private Data data;
	
	private String msg;
	
	
	public String getCode() {
		return code;
	}
	public Data getData() {
		return data;
	}
	public void setData(Data data) {
		this.data = data;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	
}
